public class Ocena {
    private String przedmiot;
    private int ocena;

    public Ocena(String przedmiot, int ocena) {
        this.przedmiot = przedmiot;
        this.ocena = ocena;
    }

    public String getPrzedmiot() {
        return przedmiot;
    }

    public int getOcena() {
        return ocena;
    }
}
